# basic_info.py

# 1. Запрашиваем имя и профессию
name = input("Введите ваше имя: ").strip()
profession = input("Введите вашу профессию: ").strip()

# 2. Запрашиваем стаж в QA (лет)
experience = input("Сколько лет вы работаете в QA? ").strip()

# 3. Проверяем, знает ли пользователь, что такое переменная
answer = input("Что такое переменная? ").strip().lower()

# 4. Выводим результаты
print(f"\nПривет, {name}! Ты работаешь в роли {profession} уже {experience} лет.")

if answer == "переменная":
    print("Верно — переменная хранит значение, которое может изменяться.")
else:
    print("Неправильно — переменная это имя, связанное с данными (например: x = 5).")
